from sentence_exercises
where exercise_set_id is not null
on conflict (exercise_set_id, sentence_exercise_id) do nothing;

alter table sentence_exercises alter column exercise_set_id drop not null;
comment on column sentence_exercises.exercise_set_id is '兼容字段：题组ID，新逻辑以 exercise_set_items 为准';
comment on column sentence_exercises.sort_order is '兼容字段：默认排序，新逻辑可在 exercise_set_items 中按题组覆盖';

drop index if exists idx_sentence_exercises_set_order;
create index if not exists idx_exercise_set_items_set_order on exercise_set_items(exercise_set_id, sort_order, sentence_exercise_id);
create index if not exists idx_exercise_set_items_exercise on exercise_set_items(sentence_exercise_id);
create index if not exists idx_sentence_exercises_status_answer on sentence_exercises(status, hanzi_answer);
