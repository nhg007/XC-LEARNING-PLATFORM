create table if not exists user_sentence_progress (
  id bigint generated by default as identity,
  user_id bigint not null,
  exercise_set_id bigint not null,
  sentence_exercise_id bigint not null,
  status varchar(30) not null default 'learning',
  attempt_count integer not null default 0,
  correct_count integer not null default 0,
  learned_at timestamptz,
  last_practiced_at timestamptz,
  last_correct_at timestamptz,
  next_review_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint pk_user_sentence_progress primary key (id),
  constraint fk_user_sentence_progress_user foreign key (user_id) references users(id),
  constraint fk_user_sentence_progress_set foreign key (exercise_set_id) references exercise_sets(id),
  constraint fk_user_sentence_progress_exercise foreign key (sentence_exercise_id) references sentence_exercises(id),
  constraint uq_user_sentence_progress_user_exercise unique (user_id, sentence_exercise_id),
  constraint ck_user_sentence_progress_status check (status in ('learning', 'learned', 'reviewing', 'mastered')),
  constraint ck_user_sentence_progress_counts check (attempt_count >= 0 and correct_count >= 0 and correct_count <= attempt_count)
);

