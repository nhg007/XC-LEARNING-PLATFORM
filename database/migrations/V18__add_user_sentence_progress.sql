create index if not exists idx_user_sentence_progress_user_set on user_sentence_progress(user_id, exercise_set_id);
create index if not exists idx_user_sentence_progress_exercise on user_sentence_progress(sentence_exercise_id);
create index if not exists idx_user_sentence_progress_status on user_sentence_progress(status);

insert into user_sentence_progress (
  user_id,
  exercise_set_id,
  sentence_exercise_id,
  status,
  attempt_count,
  correct_count,
  learned_at,
  last_practiced_at,
  last_correct_at,
  created_at,
  updated_at
)
select
  attempts.user_id,
  exercises.exercise_set_id,
  attempts.exercise_id,
  case when sum(case when attempts.is_correct then 1 else 0 end) > 0 then 'learned' else 'learning' end,
  count(*)::integer,
  sum(case when attempts.is_correct then 1 else 0 end)::integer,
  min(attempts.answered_at) filter (where attempts.is_correct),
  max(attempts.answered_at),
  max(attempts.answered_at) filter (where attempts.is_correct),
  min(coalesce(attempts.created_at, attempts.answered_at, now())),
  max(coalesce(attempts.updated_at, attempts.answered_at, now()))
from exercise_attempts attempts
join sentence_exercises exercises on exercises.id = attempts.exercise_id
group by attempts.user_id, exercises.exercise_set_id, attempts.exercise_id
on conflict (user_id, sentence_exercise_id) do nothing;
