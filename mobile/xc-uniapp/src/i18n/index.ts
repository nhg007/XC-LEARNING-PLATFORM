const locale = ref<MobileLocale>(resolveInitialLocale())

export const supportedLocales = (Object.keys(localeLabels) as MobileLocale[]).map(value => ({
  value,
  label: localeLabels[value]
}))

export const currentLocale = computed(() => locale.value)

export function useI18n() {
  return {
    locale: currentLocale,
    supportedLocales,
    setLocale,
    t
  }
}

export function t(key: string, params: Record<string, string | number> = {}) {
  const template = messages[locale.value][key] || messages['zh-CN'][key] || key
  return Object.entries(params).reduce((text, [name, value]) => {
    return text.replaceAll(`{${name}}`, String(value))
  }, template)
}

export function setLocale(value: MobileLocale) {
  locale.value = value
  uni.setStorageSync(LOCALE_KEY, value)
  applyTabBarLocale()
}

export function applyTabBarLocale() {
  const tabKeys = ['tab.home', 'tab.features', 'tab.more', 'tab.profile']
  tabKeys.forEach((key, index) => {
    uni.setTabBarItem({ index, text: t(key) })
  })
}

export function setPageTitle(key: string) {
  uni.setNavigationBarTitle({ title: t(key) })
}

function resolveInitialLocale(): MobileLocale {
  const stored = uni.getStorageSync(LOCALE_KEY) as MobileLocale
  if (stored && stored in localeLabels) {
    return stored
  }
  const systemLocale = uni.getSystemInfoSync().language || ''
  if (systemLocale.startsWith('ru')) {
    return 'ru'
  }
  if (systemLocale.startsWith('en')) {
    return 'en'
  }
  return 'zh-CN'
}
